#include "libft.h"

int	ft_atoi(const char *str)
{
	int		sign;
	long	sum;

	sign = 1;
	sum = 0;
	while ((*str == '\t') || (*str == '\n') || (*str == '\v') || (*str == '\f')
		|| (*str == '\r') || (*str == ' '))
		str++;
	if ((*str == '-') || (*str == '+'))
	{
		if (*str == '-')
			sign = -1;
		str++;
	}
	while ((*str >= '0') && (*str <= '9'))
	{
		sum = sum * 10 + (sign * (*str - 48));
		str++;
	}
	return ((int)sum);
}
