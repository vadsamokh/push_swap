#include "libft.h"

int	ft_strcmp(char *s1, char *s2)
{
	int	a;

	a = 0;
	while ((*s1 != '\0') || (*s2 != '\0'))
	{
		if (*s1 > *s2)
		{
			a = 1;
			break ;
		}
		if (*s1 < *s2)
		{
			a = -1;
			break ;
		}
		s1++;
		s2++;
	}
	return (a);
}
